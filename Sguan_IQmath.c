/**
 * @brief  浮点数转Q31定点(带标幺化)
 * @param  f: 待转换浮点数
 * @param  base_value: 基准值（标幺化分母）
 * @retval Q31定点数
 */
Q31_t IQmath_Q31_from_float(float f, float base_value){
    // 1. 参数验证
    double scaled;
    if (base_value <= 0.0f || f != f){  // f != f 检查NaN
        return Q31_ZERO;
    }
    else if(base_value != 1.0f){
        // 2. 标幺化
        float normalized = f / base_value;
        // 3. 饱和处理
        if (normalized >= 1.0f){
            return Q31_MAX;
        } else if (normalized <= -1.0f){
            return Q31_MIN;
        }
        // 4. 转换为Q31
        // 使用双精度提高精度：normalized * 2^31
        scaled = (double)normalized * 2147483648.0;  // 2^31
    }
    else{
        scaled = (double)f * 2147483648.0;
    }
    // 5. 四舍五入
    if (scaled >= 0.0){
        scaled += 0.5;
    } else{
        scaled -= 0.5;
    }
    // 6. 转换为整数
    int64_t result = (int64_t)scaled;
    // 7. 范围检查（防止因四舍五入溢出）
    if (result > Q31_MAX_64){
        return Q31_MAX;
    } else if (result < Q31_MIN_64){
        return Q31_MIN;
    }
    return (Q31_t)result;
}

/**
 * @brief  Q31定点转浮点数(带反标幺化)
 * @param  q: Q31定点数
 * @param  base_value: 基准值（反标幺化乘数）
 * @retval 转换后的浮点数
 */
float IQmath_Q31_to_float(Q31_t q, float base_value){
    // 1. 参数检查
    if (base_value <= 0.0f){
        return 0.0f;
    }
    // 2. Q31转归一化浮点
    double normalized = (double)q * Q31_SCALE;
    // 3. 反标幺化
    return (float)(normalized * base_value);
}

/**
 * @brief  Q31定点数乘法（带舍入和饱和）
 * @param  a: 乘数1（Q1.31格式）
 * @param  b: 乘数2（Q1.31格式）
 * @retval 乘法结果（Q1.31格式）
 */
Q31_t IQmath_Q31_mul(Q31_t a, Q31_t b){
    int64_t result = (int64_t)a * (int64_t)b;
    result += 1LL << 30;    // 四舍五入：加0.5*2^31=1<<30
    result >>= 31;          // 右移31位
    // 饱和检查
    if (result > Q31_MAX_64){
        return Q31_MAX;
    }
    if (result < Q31_MIN_64){
        return Q31_MIN;
    }
    return (Q31_t)result;
}

/**
 * @brief  Q31定点数除法（带舍入和饱和）
 * @param  a: 被除数（Q1.31格式）
 * @param  b: 除数（Q1.31格式）
 * @retval 除法结果（Q1.31格式）
 */
Q31_t IQmath_Q31_div(Q31_t a, Q31_t b){
    if (b == 0){
        // 除零：返回最大值或最小值
        return (a >= 0) ? Q31_MAX : Q31_MIN;
    }
    int64_t numerator = (int64_t)a << 31;
    int64_t result = numerator / b;
    // 计算余数用于四舍五入
    int64_t remainder = numerator % b;
    // 四舍五入
    if (remainder != 0){
        // 取余数的绝对值
        int64_t rem_abs = (remainder < 0) ? -remainder : remainder;
        int64_t b_abs = (b < 0) ? -(int64_t)b : (int64_t)b;
        // 如果余数的两倍大于等于除数，需要进位
        if (rem_abs * 2 >= b_abs){
            result += (numerator >= 0) ? 1 : -1;
        }
    }
    // 饱和检查
    if (result > Q31_MAX_64){
        return Q31_MAX;
    }
    if (result < Q31_MIN_64){
        return Q31_MIN;
    }
    return (Q31_t)result;
}

/**
 * @brief  Q31定点数加法（带饱和检查）
 * @param  a: 加数1（Q1.31格式）
 * @param  b: 加数2（Q1.31格式）
 * @retval 加法结果（Q1.31格式）
 */
Q31_t IQmath_Q31_add(Q31_t a, Q31_t b){
    int32_t sum = a + b;
    // 检查溢出：正数+正数=负数 表示正溢出
    if ((a > 0) && (b > 0) && (sum <= 0)){
        return Q31_MAX;
    }
    // 检查溢出：负数+负数=正数 表示负溢出
    if ((a < 0) && (b < 0) && (sum >= 0)){
        return Q31_MIN;
    }
    return sum;
}

/**
 * @brief  Q31定点数减法（带饱和检查）
 * @param  a: 被减数（Q1.31格式）
 * @param  b: 减数（Q1.31格式）
 * @retval 减法结果（Q1.31格式）
 * 
 */
Q31_t IQmath_Q31_sub(Q31_t a, Q31_t b){
    int32_t diff = a - b;
    // 检查溢出：正数-负数=负数 表示正溢出
    if ((a > 0) && (b < 0) && (diff <= 0)){
        return Q31_MAX;
    }
    // 检查溢出：负数-正数=正数 表示负溢出
    if ((a < 0) && (b > 0) && (diff >= 0)){
        return Q31_MIN;
    }
    return diff;
}

/**
 * @brief  Q31在不同基准值之间转换（直接计算，避免浮点误差）
 * @param  q: 源Q31值
 * @param  src_base: 源基准值
 * @param  dst_base: 目标基准值
 * @retval 转换后的Q31值
 */
Q31_t IQmath_Q31_convert_base(Q31_t q, float src_base, float dst_base){
    if (src_base <= 0.0f || dst_base <= 0.0f){
        return 0;
    }
    
    // 1. 如果基准值相同，直接返回
    if (Value_fabsf(src_base - dst_base) < 1e-6f){
        return q;
    }
    
    // 2. 直接使用比例系数转换
    // 原理：实际值 = q * src_base / 2^31
    //      新Q31 = 实际值 * 2^31 / dst_base = q * (src_base / dst_base)
    double ratio = (float)src_base / (float)dst_base;
    double result = (double)q * ratio;
    if (result > 2147483647.0){
        return Q31_MAX;  // Q31_MAX
    }
    if (result < -2147483648.0){
        return Q31_MIN;  // Q31_MIN
    }
    
    if (result >= 0){
        result += 0.5;
    } else{
        result -= 0.5;
    }
    
    return (Q31_t)result;
}

