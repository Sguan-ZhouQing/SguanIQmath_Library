// 快速正弦算法
Q31_t fast_sin_q31(float theta) {
    while (theta >= Value_2PI) {
        theta -= Value_2PI;
    }
    while (theta < 0.0f) {
        theta += Value_2PI;
    }
    float idx_f = theta * (512.0f / Value_2PI);
    int idx = (int)idx_f;
    
    // 边界保护
    if (idx >= 512) idx = 511;
    if (idx < 0) idx = 0;
    
    return sin_tab_q31[idx];
}

// 快速求解sine和cosine
void fast_sin_cos_q31(float x, Q31_t *sin_x, Q31_t *cos_x) {
  *sin_x = fast_sin_q31(x);
  *cos_x = fast_cos_q31(x);
}


