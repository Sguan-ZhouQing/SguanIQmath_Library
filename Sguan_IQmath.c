// 快速正弦算法
float fast_sin(float theta) {
    while (1) {
        if (theta > 6.2831854f)
            theta = theta - 6.2831854f;
        else if (theta < 0)
            theta = theta + 6.2831854f;
        else
            break;
    }
    float idx_f = theta * Value_512_2PI;
    int idx = (int)idx_f;
    
    // 边界保护
    if (idx >= 512) idx = 511;
    if (idx < 0) idx = 0;
    
    return sin_tab[idx];
}

// 快速求解sine和cosine
void fast_sin_cos(float x, float *sin_x, float *cos_x) {
  *sin_x = fast_sin(x);
  *cos_x = fast_cos(x);
}

