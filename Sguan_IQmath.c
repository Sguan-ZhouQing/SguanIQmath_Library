// 读寄存器位的实际数值
uint8_t IQmath_ReadBit(uint8_t reg, uint8_t n){
    return (reg & (1 << n)) ? 1 : 0;
}

// 写寄存器为1或者0(单独写)
void IQmath_SetBit(uint8_t *reg, uint8_t n, uint8_t Bit){
    if (Bit){
        *reg |= (1 << n);
    }
    else{
        *reg &= ~(1 << n);
    }
}
