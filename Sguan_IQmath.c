// int32_t整数开平方（查表法，求整数平方根）
Q31_t Value_sqrt_q31(Q31_t input){
    int64_t x = (int64_t)input << 31;
    // 负数直接返回 0
    if (x <= 0) return 0;
    
    // 初始估计：使用最高位位置估算
    int64_t guess = 1;
    
    // 快速找到最高位位置（类似log2）
    // 对于32位以上，使用位移快速估算初始值
    if (x > (1LL << 60)) {
        guess = 1LL << 30;
    } else if (x > (1LL << 50)) {
        guess = 1LL << 25;
    } else if (x > (1LL << 40)) {
        guess = 1LL << 20;
    } else if (x > (1LL << 30)) {
        guess = 1LL << 15;
    } else if (x > (1LL << 20)) {
        guess = 1LL << 10;
    } else if (x > (1LL << 10)) {
        guess = 1LL << 5;
    }
    
    // 牛顿迭代法： guess = (guess + x/guess) / 2
    // 迭代4-6次即可达到足够精度
    for (int i = 0; i < 6; i++) {
        int64_t new_guess = (guess + x / guess) >> 1;
        // 当误差小于1时停止迭代
        if (new_guess == guess || new_guess == guess - 1) {
            break;
        }
        guess = new_guess;
    }
    
    // 返回整数部分
    return (Q31_t)guess;
}

