// ===================== Q17.15 常量定义 =====================
#define Q15_SHIFT        15                    // 小数位占15位
#define Q15_MULTIPLIER   (1 << Q15_SHIFT)      // 32768（2^15）
#define Q15_MAX_FLOAT    (Q15_INT_MAX + (Q15_MULTIPLIER - 1.0f) / Q15_MULTIPLIER)
#define Q15_MIN_FLOAT    (float)Q15_INT_MIN    // -65536.0
